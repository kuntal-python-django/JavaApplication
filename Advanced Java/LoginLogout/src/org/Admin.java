package org;

public class Admin 
{
	private int id;  
	private String name;  
	private float password;
	
	public int getId() 
	{
		return id;
	}
	public void setId(int id) 
	{
		this.id = id;
	}
	public String getName() 
	{
		return name;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public float getPassword() 
	{
		return password;
	}
	public void setPassword(float password) 
	{
		this.password = password;
	}
	
}
